function  [in, bp, wls] = a1_input_data(dataSE)
 


%-------------------Flat Start Magnitude and Angle-------------------------
 fmean = 0;
 fvari = 10^20;
%--------------------------------------------------------------------------


%--------------------------Pi Model and Y Bus------------------------------
 [in] = a2_pi_model(dataSE);
 [in] = a3_ybus(in);
%--------------------------------------------------------------------------


%------------------------Direct Measurements-------------------------------
 [bp, in] = a4_direct_meas(in, fmean, fvari);
%--------------------------------------------------------------------------


%----------------------Indirect Measurements-------------------------------
 [bp] = a5_indirect_meas(in, bp);
%--------------------------------------------------------------------------


%-----------------------------Constant-------------------------------------
 bp.variable = in.Nbu;
 bp.factor   = size(bp.mean_ang,2) + size(bp.mean,1);
 bp.indir    = size(bp.mean,1);
 bp.direc    = size(bp.mean_ang,2);
 bp.idx      = find(bp.A ~= 0);
%--------------------------------------------------------------------------


%---------------------------------WLS--------------------------------------
 [wls] = a6_wls(bp, in);
%--------------------------------------------------------------------------


%------------------------Check Connectivity--------------------------------
 [bp] = a7_connect(bp, in.Nbu, fmean, fvari);
%--------------------------------------------------------------------------


%--------------------------------Mask--------------------------------------
 bp.Hm = ones(bp.variable, bp.variable);
 bp.Hm(logical(eye(size(bp.Hm)))) = 0;
  
 bp.Fm = ones(bp.factor, bp.factor);
 bp.Fm(logical(eye(size(bp.Fm)))) = 0;
 bp.Fm = bp.Fm';
%--------------------------------------------------------------------------
