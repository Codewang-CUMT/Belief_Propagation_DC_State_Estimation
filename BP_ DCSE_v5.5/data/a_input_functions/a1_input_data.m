 function  [sys, bp, wls] = a1_input_data(dataSE, user)
  
 
 
 tic
%--------------------------Pi Model and Y Bus------------------------------
 [sys] = a2_pi_model(dataSE.system);
 [sys] = a3_ybus(sys);
%--------------------------------------------------------------------------


%--------------------Number of Variable and Factor Nodes-------------------
 [bp, dataSE] = a4_adjust(sys, dataSE);
%--------------------------------------------------------------------------


%--------------------------Local Factor Nodes------------------------------
 [sys, bp] = a5_local_factor(sys, dataSE.pmu, bp, user);
%--------------------------------------------------------------------------


%----------------------Indirect Factor Nodes-------------------------------
 [bp] = a6_indirect_factor(sys, dataSE.analog, bp);
%--------------------------------------------------------------------------


%--------------------------------Mask--------------------------------------
 [bp] = a7_mask(bp);
%--------------------------------------------------------------------------


%-------------------------Convergence Parameters---------------------------
 [bp] = a8_converg(user, bp);
%--------------------------------------------------------------------------


%---------------------------------WLS--------------------------------------
 [wls] = a9_wls(sys, bp);
%--------------------------------------------------------------------------


%------------------------Check Connectivity--------------------------------
 [bp] = a10_connect(bp);
%--------------------------------------------------------------------------
 

%-------------------Preprocesing Time - Part 1-----------------------------
 bp.pre_time = toc;
%-------------------------------------------------------------------------- 